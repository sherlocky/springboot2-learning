package com.sherlocky.springboot2.testng;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

/**
 * TestNG 是一个测试框架，其灵感来自 JUnit 和 NUnit，但引入了一些新的功能，使其功能更强大，使用更方便。
 *
 * 是一个开源自动化测试框架;TestNG表示下一代(Next Generation的首字母)。
 *
 * TestNG类似于 JUnit (特别是 JUnit 4)，但它不是JUnit框架的扩展。
 *
 * 它的灵感来源于 JUnit 。它的目的是优于 JUnit ，尤其是在用于测试集成多类时。
 */
@SpringBootApplication
public class Springboot2TestngReportApplication {
    public static void main(String[] args) {
        SpringApplication.run(Springboot2TestngReportApplication.class, args);
    }
}

